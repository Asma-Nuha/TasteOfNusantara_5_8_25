        <h2 class="text-2xl font-bold mb-4 text-gray-800">Cara Membuat</h2>

        <h3 class="text-lg font-semibold mb-2 text-gray-700">Membuat Ayam Betutu</h3>
        <ol class="list-decimal pl-6 text-gray-600">
          <li>
            Menyiapkan Bumbu: Haluskan bumbu halus menggunakan blender atau cobek hingga
            benar-benar halus.
          </li>
          <li>
            Membumbui Ayam: Panaskan minyak dalam wajan, tumis bumbu halus hingga harum dan matang.
            Tambahkan serai, daun salam, daun jeruk, dan lengkuas. Tumis hingga bumbu menjadi lebih
            harum. Angkat bumbu tumis dan biarkan sedikit dingin.
          </li>
          <li>
            Membumbui Ayam: Lumuri ayam dengan bumbu tumis hingga merata. Pastikan bumbu masuk ke
            dalam bagian dalam ayam. Diamkan ayam dalam lemari es selama minimal 1 jam agar bumbu
            meresap. Jika memungkinkan, diamkan semalaman.
          </li>
          <li>
            Membungkus dan Memanggang: Siapkan daun pisang, letakkan ayam yang sudah dibumbui di
            atas daun pisang. Bungkus ayam dengan daun pisang, lalu ikat dengan tali agar bungkusan
            tetap rapat. Panggang ayam dalam oven 180Â°C selama 1,5 - 2 jam, atau masak dengan bara
            api.
          </li>
          <li>
            Penyajian: Setelah ayam betutu matang, buka bungkusan daun pisang dan sajikan dengan
            nasi putih hangat serta pelengkap.
          </li>
        </ol>
