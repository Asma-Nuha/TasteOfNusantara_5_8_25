        <h3 class="text-lg font-semibold mb-2 text-gray-700">Membuat Bakso</h3>
        <ol class="list-decimal pl-6 text-gray-600">
          <li>
            Haluskan bumbu halus menggunakan blender atau cobek hingga
            benar-benar halus.
          </li>
        </ol>

        <h3 class="text-lg font-semibold mt-6 mb-2 text-gray-700">Membuat Rendang</h3>
        <ol class="list-decimal pl-6 text-gray-600">
          <li>
            Panaskan minyak goreng dalam wajan besar. Tumis bumbu halus hingga
            harum dan matang.
          </li>
          <li>
            Masukkan daun jeruk, daun salam, serai, lengkuas, dan jahe. Tumis
            hingga bumbu dan rempah mulai mengeluarkan aroma.
          </li>
          <li>
            Masukkan potongan daging sapi, aduk hingga daging berubah warna dan
            tercampur rata dengan bumbu.
          </li>
          <li>
            Tuangkan santan kental ke dalam wajan, aduk rata. Masak dengan api
            kecil sambil sesekali diaduk agar santan tidak pecah.
          </li>
          <li>
            Tambahkan asam kandis atau air asam jawa. Masak rendang dengan api
            kecil hingga daging empuk dan kuah mengental, sekitar 2-3 jam.
            Proses memasak yang lama akan membuat daging menjadi sangat empuk
            dan bumbu meresap sempurna.
          </li>
          <li>
            Koreksi rasa dengan garam dan gula merah, tambahkan jika diperlukan.
          </li>
          <li>
            Setelah rendang matang dan kuah mengental, angkat dan sajikan dalam
            mangkuk atau piring saji.
          </li>
        </ol>
